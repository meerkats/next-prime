const sum = (value1: number, value2: number) => value1 + value2;
const difference = (value1: number, value2: number) => value1 - value2;
const product = (value1: number, value2: number) => value1 * value2;
const quotient = (value1: number, value2: number) => value1 / value2;
const mod = (value1: number, value2: number) => value1 % value2;

export { sum, difference, product, quotient, mod };
